void main() {
  //loops
  for (var i = 2; i < 5; i++) {
    print(i);
  }

  var a = <String>["ali", "john", "mary"];
  for (var o in a) {
    print(o);
  }

  // while
  var i = 2;
  while (i < 5) {
    print(i);
    i++;
  }

  //do while

  var u = 2;

  do {
    print(u);
    u++;
  } while (u <= 5);
}
